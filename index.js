export async function assignCivilRole(member) {
  try {
    const guild = member.guild;
    
    // Busca flex√≠vel pelo cargo Civis
    const civilRole = guild.roles.cache.find(r => 
      r.name === "Civis" || 
      r.name.toLowerCase().includes("civil") ||
      (r.name.toLowerCase().includes("membro") && !r.name.toLowerCase().includes("verificado"))
    );
    
    if (!civilRole) {
      console.warn(`‚ùå Cargo 'Civis' n√£o encontrado no servidor: ${guild.name}!`);
      return false;
    }

    // Verifica permiss√µes do bot
    if (!guild.members.me.permissions.has(PermissionFlagsBits.ManageRoles)) {
      console.warn(`‚ùå Bot sem permiss√£o para gerenciar cargos no servidor: ${guild.name}`);
      return false;
    }

    if (civilRole.position >= guild.members.me.roles.highest.position) {
      console.warn(`‚ùå Cargo Civis est√° acima do cargo do bot no servidor: ${guild.name}`);
      return false;
    }

    if (member.roles.cache.has(civilRole.id)) {
      return true;
    }

    await member.roles.add(civilRole);
    console.log(`‚úÖ Cargo Civis atribu√≠do automaticamente para: ${member.user.tag} no servidor: ${guild.name}`);
    
    return true;
  } catch (error) {
    console.error(`‚ùå Erro ao atribuir cargo Civis no servidor ${member.guild.name}:`, error);
    return false;
  }
}

